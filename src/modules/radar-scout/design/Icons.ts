// Helper pour injecter un icône
export function injectIcon(element: HTMLElement, iconName: keyof typeof Icons): void {
  const svg = Icons[iconName];
  if (svg) {
    element.innerHTML = svg;
  }
}

// Helper pour créer un élément icône
export function createIcon(iconName: keyof typeof Icons, className: string = ''): HTMLElement {
  const span = document.createElement('span');
  span.className = className;
  span.innerHTML = Icons[iconName];
  return span;
}
